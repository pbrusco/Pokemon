#!/usr/bin/env node
/**
 * extract-game-data.mjs
 *
 * Auto-extracts every gameplay-relevant FireRed data structure into
 * generated TypeScript modules under src/data/firered/. The runtime then
 * consumes these without any per-map hand-tuning.
 *
 *   firedTrainers.generated.ts    — gTrainers + sParty_* tables → JS objects
 *   firedNpcs.generated.ts        — per-map object_events (positions, gfx,
 *                                   movement type, trainer link, script ref)
 *   firedWildEncounters.generated.ts — per-map land/water/rock/fishing tables
 *   firedItems.generated.ts       — items.json (id, price, description)
 *   firedSigns.generated.ts       — per-map bg_events with resolved text
 */

import fs from 'fs';
import path from 'path';

const ROOT = path.resolve('.');
const FR = path.join(ROOT, 'pokefirered_dissasembly');
const OUT = path.join(ROOT, 'src/data/firered');
fs.mkdirSync(OUT, { recursive: true });

// ─── Helpers ──────────────────────────────────────────────────────────────
function read(p) { return fs.readFileSync(path.join(FR, p), 'utf8'); }
function readJson(p) { return JSON.parse(read(p)); }
function writeTs(name, contents) {
  fs.writeFileSync(path.join(OUT, name), `// AUTOGENERATED by scripts/extract-game-data.mjs — do not edit.\n\n${contents}`);
}

// ─── 1. Wild encounters ──────────────────────────────────────────────────
function extractWildEncounters() {
  const data = readJson('src/data/wild_encounters.json');
  // FireRed group is the first entry; LeafGreen is the second. We use
  // FireRed encounters for canonicity.
  const fireredGroup = data.wild_encounter_groups[0];
  const out = {};
  for (const map of fireredGroup.encounters) {
    if (!map.base_label.endsWith('_FireRed')) continue;
    const mapId = map.map; // e.g., MAP_PALLET_TOWN
    const tables = {};
    for (const field of ['land_mons', 'water_mons', 'rock_smash_mons', 'fishing_mons']) {
      if (map[field]?.mons) {
        tables[field] = {
          encounter_rate: map[field].encounter_rate ?? 0,
          mons: map[field].mons,
        };
      }
    }
    if (Object.keys(tables).length > 0) out[mapId] = tables;
  }
  writeTs(
    'firedWildEncounters.generated.ts',
    `export const FIRERED_WILD_ENCOUNTERS = ${JSON.stringify(out, null, 2)} as const;\n`
  );
  return Object.keys(out).length;
}

// ─── 2. Trainer parties + headers ────────────────────────────────────────
// Parse trainer_parties.h:  static const struct ... sParty_<Name>[] = { { ... }, { ... } };
// Then parse trainers.h:  [TRAINER_<ID>] = { .trainerClass = ..., .party = ..._MOVES(sParty_<Name>) }
function extractTrainers() {
  const partiesSrc = read('src/data/trainer_parties.h');
  const trainersSrc = read('src/data/trainers.h');

  // Parties: capture struct type so we know whether the entry has items / custom moves.
  // Use matchAll to avoid the lastIndex pitfalls of reused /g RegExp objects.
  // Skip macro-only parties (DUMMY_TRAINER_MON, etc.) — those are leftover
  // RS-only entries that FRLG never populated with real data.
  const parties = {};
  const partyMatches = partiesSrc.matchAll(/static const struct (\w+)\s+sParty_(\w+)\[\]\s*=\s*{([\s\S]*?)};/g);
  for (const m of partyMatches) {
    const [, struct, name, body] = m;
    const mons = [];
    for (const mm of body.matchAll(/\{([^}]+)\}/g)) {
      const inner = mm[1];
      const obj = {};
      for (const fm of inner.matchAll(/\.(\w+)\s*=\s*([^,}]+?)(?=,|\s*$)/g)) {
        const k = fm[1];
        const v = fm[2].trim();
        if (/^-?\d+$/.test(v)) obj[k] = parseInt(v, 10);
        else obj[k] = v;
      }
      if (Object.keys(obj).length > 0) mons.push(obj);
    }
    if (mons.length === 0) continue; // RS-only macro placeholder, skip
    parties[name] = { struct, mons };
  }

  // Trainers: [TRAINER_X] = { ...multiline... \n}, — match up to the close
  // brace on its own line. `[\s\S]*?\n {4}}` captures the multiline body
  // without getting tripped up by nested `},` from `.items = {},`.
  const trainers = {};
  for (const m of trainersSrc.matchAll(/\[TRAINER_(\w+)\]\s*=\s*{([\s\S]*?)\n {4}}/g)) {
    const [, id, body] = m;
    const out = { id: 'TRAINER_' + id };
    const classMatch = body.match(/\.trainerClass\s*=\s*TRAINER_CLASS_(\w+)/);
    if (classMatch) out.trainerClass = classMatch[1];
    const nameMatch = body.match(/\.trainerName\s*=\s*_\("([^"]*)"\)/);
    if (nameMatch) out.name = nameMatch[1];
    const picMatch = body.match(/\.trainerPic\s*=\s*TRAINER_PIC_(\w+)/);
    if (picMatch) out.pic = picMatch[1];
    const doubleMatch = body.match(/\.doubleBattle\s*=\s*(TRUE|FALSE)/);
    if (doubleMatch) out.doubleBattle = doubleMatch[1] === 'TRUE';
    const partyMatch = body.match(/sParty_(\w+)/);
    if (partyMatch) out.partyKey = partyMatch[1];
    const partyKindMatch = body.match(/\.party\s*=\s*(NO_ITEM_DEFAULT_MOVES|NO_ITEM_CUSTOM_MOVES|ITEM_DEFAULT_MOVES|ITEM_CUSTOM_MOVES)/);
    if (partyKindMatch) out.partyKind = partyKindMatch[1];
    trainers['TRAINER_' + id] = out;
  }

  writeTs(
    'firedTrainers.generated.ts',
    `export interface FireredTrainerMon {
  iv?: number; lvl: number; species: string; heldItem?: string; moves?: string;
}
export interface FireredTrainerParty {
  struct: string;
  mons: FireredTrainerMon[];
}
export interface FireredTrainer {
  id: string;
  trainerClass?: string;
  name?: string;
  pic?: string;
  doubleBattle?: boolean;
  partyKey?: string;
  partyKind?: string;
}

export const FIRERED_TRAINER_PARTIES: Record<string, FireredTrainerParty> = ${JSON.stringify(parties, null, 2)};

export const FIRERED_TRAINERS: Record<string, FireredTrainer> = ${JSON.stringify(trainers, null, 2)};
`
  );
  return { partyCount: Object.keys(parties).length, trainerCount: Object.keys(trainers).length };
}

// ─── 3. Trainer-battle script index ──────────────────────────────────────
// Build a map: script_label → TRAINER_* id, so per-map object_events
// pointing at a script can be resolved to a trainer.
function extractTrainerScripts() {
  // Trainer scripts can live in two places:
  //   - data/scripts/trainers.inc (route trainers — Route3, Route4, etc.)
  //   - data/maps/<Map>/scripts.inc (per-map trainers — VForest, gyms, caves)
  // Scan both so every NPC with `trainer_sight_or_berry_tree_id > 0` finds
  // the right TRAINER_* enum.
  const sources = [path.join(FR, 'data/scripts/trainers.inc')];
  const mapsDir = path.join(FR, 'data/maps');
  for (const dir of fs.readdirSync(mapsDir)) {
    const p = path.join(mapsDir, dir, 'scripts.inc');
    if (fs.existsSync(p)) sources.push(p);
  }

  const scriptToTrainer = {};
  for (const src of sources) {
    const text = fs.readFileSync(src, 'utf8');
    const labelRe = /^(\w+_EventScript_\w+)::/gm;
    const positions = [];
    let lm;
    while ((lm = labelRe.exec(text)) !== null) {
      positions.push({ label: lm[1], offset: lm.index });
    }
    for (let i = 0; i < positions.length; i++) {
      const { label, offset } = positions[i];
      const end = i + 1 < positions.length ? positions[i + 1].offset : text.length;
      const body = text.slice(offset, end);
      const tm = body.match(/trainerbattle_(?:single|rematch|continue_script|continue_script_no_music|double|no_intro)\s+TRAINER_(\w+)/);
      if (tm) scriptToTrainer[label] = 'TRAINER_' + tm[1];
    }
  }
  return scriptToTrainer;
}

// ─── 4. Per-map object_events → NPCs ─────────────────────────────────────
function extractNpcs(scriptToTrainer) {
  const mapsDir = path.join(FR, 'data/maps');
  const out = {};
  for (const dir of fs.readdirSync(mapsDir)) {
    const mapJson = path.join(mapsDir, dir, 'map.json');
    if (!fs.existsSync(mapJson)) continue;
    const m = JSON.parse(fs.readFileSync(mapJson, 'utf8'));
    if (!m.object_events?.length) continue;
    const npcs = m.object_events.map(o => {
      // `?? null/0` on every field — JSON.stringify drops undefined keys,
      // which would break the FireredNpc TS contract at consume sites.
      const trainerId = (o.script ? scriptToTrainer[o.script] : null) ?? null;
      return {
        localId: o.local_id ?? null,
        x: o.x ?? 0,
        y: o.y ?? 0,
        elevation: o.elevation ?? 0,
        gfx: o.graphics_id?.replace(/^OBJ_EVENT_GFX_/, '') ?? null,
        movement: o.movement_type?.replace(/^MOVEMENT_TYPE_/, '') ?? null,
        rangeX: o.movement_range_x ?? 0,
        rangeY: o.movement_range_y ?? 0,
        script: o.script ?? null,
        trainerId,
        trainerSightDistance: trainerId ? Number(o.trainer_sight_or_berry_tree_id ?? 0) : 0,
        flag: o.flag === '0' ? null : (o.flag ?? null),
      };
    });
    out[m.id] = npcs;
  }
  writeTs(
    'firedNpcs.generated.ts',
    `export interface FireredNpc {
  localId: string | null;
  x: number; y: number; elevation: number;
  gfx: string | null;
  movement: string | null;
  rangeX: number; rangeY: number;
  script: string | null;
  trainerId: string | null;
  trainerSightDistance: number;
  flag: string | null;
}
export const FIRERED_NPCS: Record<string, FireredNpc[]> = ${JSON.stringify(out, null, 2)};
`
  );
  return Object.keys(out).length;
}

// ─── 5. Per-map signs (bg_events) ─────────────────────────────────────────
function extractSigns() {
  const mapsDir = path.join(FR, 'data/maps');
  const out = {};
  for (const dir of fs.readdirSync(mapsDir)) {
    const mapJson = path.join(mapsDir, dir, 'map.json');
    if (!fs.existsSync(mapJson)) continue;
    const m = JSON.parse(fs.readFileSync(mapJson, 'utf8'));
    if (!m.bg_events?.length) continue;
    const signs = m.bg_events.map(b => ({
      type: b.type,
      x: b.x, y: b.y, elevation: b.elevation,
      script: b.script ?? null,
      item: b.item ?? null,
      flag: b.flag ?? null,
    }));
    out[m.id] = signs;
  }
  writeTs(
    'firedSigns.generated.ts',
    `export interface FireredSign {
  type: string;
  x: number; y: number; elevation: number;
  script: string | null;
  item: string | null;
  flag: string | null;
}
export const FIRERED_SIGNS: Record<string, FireredSign[]> = ${JSON.stringify(out, null, 2)};
`
  );
  return Object.keys(out).length;
}

// ─── 6. Species data (types, base stats, catch rate) ────────────────────
function extractSpecies() {
  const src = read('src/data/pokemon/species_info.h');
  const out = {};
  for (const m of src.matchAll(/\[SPECIES_(\w+)\]\s*=\s*{([\s\S]*?)\n {4}}/g)) {
    const [, id, body] = m;
    if (id === 'NONE' || id === 'EGG' || id === 'OLD_UNOWN_B' || id === 'OLD_UNOWN_C' ||
        id === 'OLD_UNOWN_D' || id === 'OLD_UNOWN_E') continue;
    const get = (re, parse = parseInt) => {
      const mm = body.match(re);
      return mm ? parse(mm[1], 10) : null;
    };
    const typesMatch = body.match(/\.types\s*=\s*{TYPE_(\w+),\s*TYPE_(\w+)}/);
    const types = typesMatch ? [typesMatch[1], typesMatch[2]] : null;
    out[id] = {
      baseHP: get(/\.baseHP\s*=\s*(\d+)/),
      baseAttack: get(/\.baseAttack\s*=\s*(\d+)/),
      baseDefense: get(/\.baseDefense\s*=\s*(\d+)/),
      baseSpeed: get(/\.baseSpeed\s*=\s*(\d+)/),
      baseSpAttack: get(/\.baseSpAttack\s*=\s*(\d+)/),
      baseSpDefense: get(/\.baseSpDefense\s*=\s*(\d+)/),
      types,
      catchRate: get(/\.catchRate\s*=\s*(\d+)/),
      expYield: get(/\.expYield\s*=\s*(\d+)/),
    };
  }
  writeTs(
    'firedSpecies.generated.ts',
    `interface FireredSpecies {
  baseHP: number; baseAttack: number; baseDefense: number;
  baseSpeed: number; baseSpAttack: number; baseSpDefense: number;
  types: [string, string] | null;
  catchRate: number;
  expYield: number;
}
export const FIRERED_SPECIES: Record<string, FireredSpecies> = ${JSON.stringify(out, null, 2)};
`
  );
  return Object.keys(out).length;
}

// ─── 7. Items table ──────────────────────────────────────────────────────
function extractItems() {
  const data = readJson('src/data/items.json');
  const out = {};
  for (const it of data.items) {
    out[it.itemId] = {
      english: it.english,
      price: it.price,
      pocket: it.pocket?.replace(/^POCKET_/, '') ?? null,
      description: it.description_english,
      holdEffect: it.holdEffect?.replace(/^HOLD_EFFECT_/, '') ?? null,
    };
  }
  writeTs(
    'firedItems.generated.ts',
    `export interface FireredItem {
  english: string;
  price: number;
  pocket: string | null;
  description: string;
  holdEffect: string | null;
}
export const FIRERED_ITEMS: Record<string, FireredItem> = ${JSON.stringify(out, null, 2)};
`
  );
  return Object.keys(out).length;
}

// ─── Main ─────────────────────────────────────────────────────────────────
const wildCount = extractWildEncounters();
const { partyCount, trainerCount } = extractTrainers();
const scriptToTrainer = extractTrainerScripts();
const npcCount = extractNpcs(scriptToTrainer);
const signCount = extractSigns();
const itemCount = extractItems();
const speciesCount = extractSpecies();

console.log(`✓ extract-game-data:`);
console.log(`  wild encounters : ${wildCount} maps`);
console.log(`  trainers        : ${trainerCount} entries (${partyCount} parties)`);
console.log(`  npcs            : ${npcCount} maps`);
console.log(`  signs           : ${signCount} maps`);
console.log(`  items           : ${itemCount} entries`);
console.log(`  species         : ${speciesCount} entries`);
